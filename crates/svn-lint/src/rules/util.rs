//! Shared helpers for rule modules.

/// Void HTML elements (no closing tag needed).
///
/// Reference: https://html.spec.whatwg.org/multipage/syntax.html#void-elements
pub fn is_void_element(tag: &str) -> bool {
    matches!(
        tag,
        "area"
            | "base"
            | "br"
            | "col"
            | "command"
            | "embed"
            | "hr"
            | "img"
            | "input"
            | "keygen"
            | "link"
            | "meta"
            | "param"
            | "source"
            | "track"
            | "wbr"
    )
}

